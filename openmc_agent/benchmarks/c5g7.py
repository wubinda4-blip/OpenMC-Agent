def _xsdata(
    name: str,
    groups: openmc.mgxs.EnergyGroups,
    *,
    total: list[float],
    absorption: list[float],
    scatter: list[list[float]],
    fission: list[float] | None = None,
    nu_fission: list[float] | None = None,
    chi: list[float] | None = None,
) -> openmc.XSdata:
    xsdata = openmc.XSdata(name, groups)
    xsdata.order = 0
    xsdata.set_total(np.array(total))
    xsdata.set_absorption(np.array(absorption))
    scatter_matrix = np.array([scatter])
    xsdata.set_scatter_matrix(np.rollaxis(scatter_matrix, 0, 3))
    if fission is not None:
        xsdata.set_fission(np.array(fission))
    if nu_fission is not None:
        xsdata.set_nu_fission(np.array(nu_fission))
    if chi is not None:
        xsdata.set_chi(np.array(chi))
    return xsdata
