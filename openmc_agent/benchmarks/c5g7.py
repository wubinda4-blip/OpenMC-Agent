def export_mgxs_hdf5(filename: str | Path = "mgxs.h5") -> Path:
    """Export the C5G7 seven-group macroscopic library for an OpenMC run."""
    path = Path(filename)
    groups = openmc.mgxs.EnergyGroups(np.logspace(-5, 7, 8))
    library = openmc.MGXSLibrary(groups)
    library.add_xsdatas(_build_xsdatas(groups))
    library.export_to_hdf5(path)
    return path


def make_macroscopic_materials() -> dict[str, openmc.Material]:
    """Create OpenMC materials backed by the C5G7 macroscopic datasets."""
    mapping = {
        "uo2": "UO2",
        "mox43": "MOX 4.3%",
        "mox7": "MOX 7.0%",
        "mox87": "MOX 8.7%",
        "fiss_chamber": "Fission Chamber",
        "guide_tube": "Guide Tube",
        "water": "Water",
    }
    materials: dict[str, openmc.Material] = {}
    for xs_name, material_name in mapping.items():
        material = openmc.Material(name=material_name)
        material.set_density("macro", 1.0)
        material.add_macroscopic(xs_name)
        materials[xs_name] = material
    return materials


